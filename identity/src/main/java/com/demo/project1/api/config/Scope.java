package com.demo.project1.api.config;

/**
 * {@code Scope} enum contains list of request scope.
 *
 * @author Nisheeth Shah
 * @since 1.0.0.RELEASE September 27, 2018
 */
public enum Scope {

    /**
     * Scope of refresh token
     */
    REFRESH_TOKEN,
    /**
     * Scope of user verification
     */
    USER_VERIFICATION,
    /**
     * Scope of user to reset password
     */
    RESET_PASSWORD
}
